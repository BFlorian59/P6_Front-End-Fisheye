class Photo_media {
    constructor(media) {
        this.id = media.id;
        this.photographerId = media.photographerId;
        this.title = media.title;
        this.file = media.image;
        this.price = media.price;
        this.date = media.date;
        this.likes = media.likes;
        this.alt = media.image;
        this.type = "img";
    }
}